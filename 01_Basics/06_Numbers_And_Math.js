// JS Math Object :
// Definition : The JavaScript Math object allows you to perform mathematical tasks on numbers.

// Examples : 

console.log(Math.round(9.841)); // Rounds A Number To It's Nearest Whole Number.

console.log(Math.trunc(414.5345334)); // Truncate Method Returns Only The Integer Part.

console.log(Math.pow(2,4)) // Returns X To The Power Y.

console.log(Math.abs(-232.4434)); // Returns The Absolute Positive Values.

console.log(Math.min(131,43,64,121)); // Returns The Smallest Value.

console.log(Math.max(131,43,64,121)); // Returns The Greatest Value.

// Math.random() : ----------------------------------------

console.log(Math.random()*10) // Prints A Number B/w 0 To 10;

console.log((Math.random()*10)+1) // Prints A Number B/w 1 To 10;

console.log(Math.trunc((Math.random()*10)+1)) // Prints A Number B/w 1 To 10;


